fun Float.toBrush(): Brush = when (this) {
    in 0.86f..1f -> Brush.verticalGradient(
        colors = listOf(
            Color.Green,
            Color.Green,
        )
    )

    in 0.51f..0.85f -> Brush.verticalGradient(
        colors = listOf(
            Color.Yellow,
            Color.Yellow
        )
    )

    in 0.01f..0.5f -> Brush.verticalGradient(
        colors = listOf(
            Color.Red,
            Color.Red
        )
    )

    else -> Brush.verticalGradient(
        colors = listOf(
            Color.Black.copy(0.32f),
            Color.Black.copy(0.32f)
        )
    )
}

